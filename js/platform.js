async function simulateLiveTelemetry() {
  const devices = dataManager.getDevices();
  if (!devices.length) return;

  const device = getRandomElement(devices);
  const newRisk = Math.max(12, Math.min(95, device.risk_score + getRandomInt(-12, 10)));
  const syncStates = ['synced', 'desynced', 'sync-in-progress'];
  const newSync = getRandomElement(syncStates);
  const newSimChanges = device.sim_changes + (Math.random() > 0.78 ? 1 : 0);
  const newTimestamp = new Date();

  await dataManager.updateDevice(device.id, {
    risk_score: newRisk,
    erp_sync_status: newSync,
    sim_changes: newSimChanges,
    last_sim_change: newSimChanges > device.sim_changes ? 0 : device.last_sim_change,
    timestamp: newTimestamp,
  });

  if (newSync === 'desynced' || newRisk > 80) {
    showToast(`Security event: ${device.model} risk changed to ${newRisk}.`);
  }

  renderAllPages();
}

function startLiveSimulation() {
  if (state.liveSimulation) return;
  if (!['dashboard', 'device-intelligence', 'fraud', 'tracking', 'customer', 'analytics'].includes(currentPage)) return;
  state.liveSimulation = true;
  simulateLiveTelemetry();
  setInterval(simulateLiveTelemetry, 6500);
}

